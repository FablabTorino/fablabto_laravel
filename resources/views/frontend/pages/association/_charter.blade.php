                    <p><strong>Che cosa è un fab lab?</strong><br>
                        I fab lab sono una rete globale di laboratori locali che facilitano lo sviluppo di invenzioni dando accesso a strumenti di fabbricazione digitale</p>

                    <p><strong>Che cosa c'è in un fab lab?</strong><br>
                        I fab lab condividono un inventario in continua evoluzione di capacità base per fare (praticamente) qualunque cosa, permettendo la condivisione alle persone ed ai progetti</p>
                    <p><strong>Che cosa fornisce la rete dei fab lab?</strong><br>
                        Assistenza operativa, educativa, tecnica, finanziaria e logistica oltre a tutto ciò che è disponibile in un singolo laboratorio</p>
                    <p><strong>Chi può usare un fab lab?</strong><br>
                        I fab lab sono disponibili come risorsa comunitaria offrendo sia accesso libero ai singoli individui sia accesso programmato per specifiche iniziative</p>
                </div>

                <div class="col-md-6">
                    <p><strong>Quali sono le tue responsabilità?</strong><br>
                        sicurezza: non fare del male alle altre persone e non danneggiare le macchine
                        operazioni: aiutare nella pulizia, mantenimento e sviluppo del laboratorio
                        conoscenza: contribuire alla documentazione ed alle istruzioni</p>
                    <p><strong>A chi appartengono le invenzioni sviluppate nei fab lab?</strong>
                        I progetti e i processi sviluppati nei fab lab possono essere protetti e venduti come l'inventore preferisce, ma dovrebbero rimanere disponibili per l'utilizzo e l'apprendimento delle altre persone</p>
                    <p><strong>Come possono fare le aziende ad usufruire dei fab lab?</strong><br>
                        Le attività commerciali possono essere prototipate e incubate in un fab lab, ma non devono entrare in conflitto con gli altri utilizzi e attività, dovrebbero crescere oltre al fab lab piuttosto che dentro al fab lab e ci si aspetta che portino benefici agli inventori, ai laboratori ed alle reti che contribuiscono al loro successo</p>
