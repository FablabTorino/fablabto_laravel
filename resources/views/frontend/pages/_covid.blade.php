                di lavoro e dei servizi igienici, prima e dopo l'utilizzo. Preghiamo gli utenti di osservare le indicazioni
                con la massima attenzione e cura per garantire a tutti serenit&agrave; e sicurezza durante l'utilizzo del lab.</p>

