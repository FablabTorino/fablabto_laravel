                L'ingresso in questi orari &egrave; consentito solo ai soci tesserati che hanno prenotato una macchina
                da utilizzare, che fanno parte di un gruppo di lavoro o che partecipano a un incontro organizzato
                da una Community.
            </p>

            <p>Per i visitatori non tesserati &egrave; consentito l'accesso solo ed esclusivamente durante gli Open Day,
