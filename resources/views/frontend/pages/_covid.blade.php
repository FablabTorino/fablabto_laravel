                un'email di prenotazione a <a href="mailto:info@fablabtorino.org">info@fablabtorino.org</a><br/>
                Non sar&agrave; permesso l'accesso ai visitatori non tesserati che si presentano in giorni e orari
                diversi da quelli indicati.</p>

            <p><strong class="text-danger">Modalit&agrave; di accesso:</strong><br/>
                L'accesso al Fablab è consentito solo ed esclusivamente:<br/>
                <ul>
                    <li>in assenza di sintomi riconducibili al Covid</li>
                    <li>indossando la mascherina</li>
                    <li>previa disinfezione delle mani con il liquido igienizzante fornito</li>
                    <li>previa misurazione della temperatura corporea inferiore ai 37,5 °C</li>
                    <li>registrando il proprio accesso sull'apposito form</li>
                </ul>
            </p>

            <p>All'ingresso del Fablab troverete una postazione con tutto il necessario e un QR code da scansionare
                per compilare il form di registrazione.</p>

            <p>Ogni utente &egrave; responsabile di eseguire la procedura di registrazione all'ingresso. I gruppi di
                lavoro e le Community dovranno individuare un referente che si assicuri che tutti i membri rispettino
                le regole e la procedura di registrazione.</p>

            <p><strong class="text-danger">Incontri delle Community:</strong><br/>
                Gli incontri delle Community continueranno a svolgersi con alcune limitazioni:
                <ul>
                    <li>La partecipazione &egrave; consentita solo ai soci tesserati</li>
                    <li>Il referente del gruppo dovr&agrave; assicurarsi che tutti i partecipanti rispettino le regole
                        di utilizzo dello spazio e si registrino all'ingresso</li>
                </ul>
            </p>

            <p><strong class="text-danger">Corsi e Workshop:</strong><br/>
                Tutti i corsi e workshop, compresi i corsi Hello World, per il momento si terranno solo in modalit&agrave; online.
                Comunicheremo le date dei prossimi workshop tramite newsletter e sui nostri canali social,
                quindi continuate a seguirci! </p>

            <p><strong class="text-danger">Capienza degli Spazi:</strong><br/>
                Abbiamo riorganizzato le attivit&agrave; negli spazi pi&ugrave; idonei del Fablab dal punto di vista
